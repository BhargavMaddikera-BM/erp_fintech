package com.blackstrawai.externalintegration.compliance.employeeprovidentfund;

public class EmployeeProvidentFundAttachmentVo {
	private Integer id;
	private String name;
	private String data;
	private String size;
	private String epfType;
	private int epfId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getEpfType() {
		return epfType;
	}
	public void setEpfType(String epfType) {
		this.epfType = epfType;
	}
	public int getEpfId() {
		return epfId;
	}
	public void setEpfId(int epfId) {
		this.epfId = epfId;
	}
	
	
}
