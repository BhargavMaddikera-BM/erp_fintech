package com.blackstrawai.export;

public class ContactsExportVo {

	private String contactSalutation;
	private String contactFirstName;
	private String contactLastName;
	private String contactWorkNo;
	private String contactMobileNo;
	private String contactEmailAddress;

	public String getContactSalutation() {
		return contactSalutation;
	}

	public void setContactSalutation(String contactSalutation) {
		this.contactSalutation = contactSalutation;
	}

	public String getContactFirstName() {
		return contactFirstName;
	}

	public void setContactFirstName(String contactFirstName) {
		this.contactFirstName = contactFirstName;
	}

	public String getContactLastName() {
		return contactLastName;
	}

	public void setContactLastName(String contactLastName) {
		this.contactLastName = contactLastName;
	}

	public String getContactWorkNo() {
		return contactWorkNo;
	}

	public void setContactWorkNo(String contactWorkNo) {
		this.contactWorkNo = contactWorkNo;
	}

	public String getContactMobileNo() {
		return contactMobileNo;
	}

	public void setContactMobileNo(String contactMobileNo) {
		this.contactMobileNo = contactMobileNo;
	}

	public String getContactEmailAddress() {
		return contactEmailAddress;
	}

	public void setContactEmailAddress(String contactEmailAddress) {
		this.contactEmailAddress = contactEmailAddress;
	}

}
